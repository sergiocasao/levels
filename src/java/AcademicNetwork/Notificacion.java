public class Notificacion {
    private int id;
    private int idProf;
    private int idAca;
    private String academia;
    private String nombre;
    private String fec;
    private String mes;
    private String diaSem;
    private String hora;
    private String diferencia;
    private boolean comPub;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the idProf
     */
    public int getIdProf() {
        return idProf;
    }

    /**
     * @param idProf the idProf to set
     */
    public void setIdProf(int idProf) {
        this.idProf = idProf;
    }

    /**
     * @return the idAca
     */
    public int getIdAca() {
        return idAca;
    }

    /**
     * @param idAca the idAca to set
     */
    public void setIdAca(int idAca) {
        this.idAca = idAca;
    }

    /**
     * @return the academia
     */
    public String getAcademia() {
        return academia;
    }

    /**
     * @param academia the academia to set
     */
    public void setAcademia(String academia) {
        this.academia = academia;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the fec
     */
    public String getFec() {
        return fec;
    }

    /**
     * @param fec the fec to set
     */
    public void setFec(String fec) {
        this.fec = fec;
    }

    /**
     * @return the mes
     */
    public String getMes() {
        return mes;
    }

    /**
     * @param mes the mes to set
     */
    public void setMes(String mes) {
        this.mes = mes;
    }

    /**
     * @return the diaSem
     */
    public String getDiaSem() {
        return diaSem;
    }

    /**
     * @param diaSem the diaSem to set
     */
    public void setDiaSem(String diaSem) {
        this.diaSem = diaSem;
    }

    /**
     * @return the hora
     */
    public String getHora() {
        return hora;
    }

    /**
     * @param hora the hora to set
     */
    public void setHora(String hora) {
        this.hora = hora;
    }

    /**
     * @return the diferencia
     */
    public String getDiferencia() {
        return diferencia;
    }

    /**
     * @param diferencia the diferencia to set
     */
    public void setDiferencia(String diferencia) {
        this.diferencia = diferencia;
    }

    /**
     * @return the comPub
     */
    public boolean isComPub() {
        return comPub;
    }

    /**
     * @param comPub the comPub to set
     */
    public void setComPub(boolean comPub) {
        this.comPub = comPub;
    }
    

   
}
